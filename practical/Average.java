// Write a program to find the average of three given values.

public class Average{
	public static void main(String[] args){
		// Variable creation.
		int a1 = 56;
		int a2 = 78;
		int a3 = 89;
		float average = (a2 + a1 + a3 ) / 3; // Computing average of numbers.
		System.out.println(" the average of three variable is : " + average);
	}
}